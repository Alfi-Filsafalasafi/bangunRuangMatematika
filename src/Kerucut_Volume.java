    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        double Jari_jari = Double.parseDouble(txt_Jari.getText());
        double tinggi = Double.parseDouble(txt_tinggi.getText());
       
        Kerucut a = new Kerucut();
        a.setJarijari(Jari_jari);
        a.setTinggi(tinggi);
        
        
        txt_hasil.setText(String.valueOf(a.volume()));
        
        txt_proses.setText("Volume = 1/3 x 22/7 x "+ a.getJarijari() +" x "+ a.getJarijari() + " x "+a.getTinggi()+"\n"
                          +"Volume = 1/3 x "+a.luas() +" x "+a.getTinggi() + "\n"
                          +"Volume = "+a.volume());
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

