    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try  {
        double alassegitiga =  Double.parseDouble(txtalassegitiga.getText());
        double tinggi =  Double.parseDouble(txttinggi.getText());
        double volume =  Double.parseDouble(txtvolume.getText());
        
        LimasSegitiga l = new LimasSegitiga();
        l.setAlas(alassegitiga);
        l.setTinggi(tinggi);
        l.setVolume(volume);
        
        txt_hasil.setText(String.valueOf(l.tinggisegitiga_v()));
        txt_proses.setText(" Tinggi Alas = " + 3 + " x " + l.getVolume() + " x " + 2 + "/" + "(" + l.getTinggi()+ " x " + l.getAlas()+ ") +\n"
                         + " Tinggi Alas = " + l.tinggisegitiga_v());
        }
        catch(Exception xx){
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + xx.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

