    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try  {
        double tinggisegitiga =  Double.parseDouble(txttinggisegitiga.getText());
        double tinggi =  Double.parseDouble(txttinggi.getText());
        double volume =  Double.parseDouble(txtvolume.getText());
        
        LimasSegitiga l = new LimasSegitiga();
        l.setTinggi(tinggisegitiga);
        l.setTinggiLimas(tinggi);
        l.setVolume(volume);
        
        txt_hasil.setText(String.valueOf(l.alassegitiga_v()));
        txt_proses.setText(" Panjang Alas = " + 3 + " x " + l.getVolume() + " x " + 2 + "/" + "(" + l.getAlassegitiga() + " x " + l.getTinggisegitiga()+ ") +\n"
                         + " Panjang Alas = " + l.alassegitiga_v());
        }
        catch(Exception xx){
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + xx.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

