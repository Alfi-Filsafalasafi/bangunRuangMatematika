    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try  {
        double alassegitiga =  Double.parseDouble(txtalassegitiga.getText());
        double tinggisegitiga =  Double.parseDouble(txttinggisegitiga.getText());
        double tinggi =  Double.parseDouble(txttinggi.getText());

        LimasSegitiga l = new LimasSegitiga();
        l.setAlas(alassegitiga);
        l.setTinggi(tinggisegitiga);
        l.setTinggiLimas(tinggi);

        
        txt_hasil.setText(String.valueOf(l.volume()));
        txt_proses.setText(" Volume = 1/3  x  1/2  x " + l.getAlas() + " x " + l.getTinggi() +  " x " + l.getTinggiLimas()+ "\n"
                          +" Volume = 1/3 x " + " ( " + l.luas()  + " x " + l.getTinggiLimas()+ " ) "+ "\n"
                          +" Volume = " + l.volume());
        }
        catch(Exception xx){
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + xx.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

