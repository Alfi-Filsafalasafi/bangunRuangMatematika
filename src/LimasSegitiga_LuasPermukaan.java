    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try  {
        double alassegitiga =  Double.parseDouble(txtalassegitiga.getText());
        double tinggisegitiga =  Double.parseDouble(txttinggisegitiga.getText());
        double tinggi =  Double.parseDouble(txttinggisisitegak.getText());
        double luas;
        
        LimasSegitiga l = new LimasSegitiga();
        l.setAlassegitiga(alassegitiga);
        l.setTinggisegitiga(tinggisegitiga);
        l.setTinggi(tinggi);
        luas = (l.getAlassegitiga() * l.getTinggisegitiga()/2);

        txt_hasil.setText(String.valueOf(l.luas_permukaan()));
        txt_proses.setText(" Luas Permukaan = ( " + l.getAlassegitiga() + " x " + l.getTinggisegitiga() +  " / 2 )" + " (3 " + " x " + l.getAlassegitiga() + " x " + l.getTinggisegitiga()+ ") +\n"
                         + " Luas Permukaan = " + l.luas_alas() + " ( " + 3 + " x " + " ( " + l.getAlassegitiga() + " x " + l.getTinggisegitiga() + " / " + 2 + " ) " + " ) " + "\n"
                         + " Luas Permukaan = " + l.luas_permukaan());
        }
        catch(Exception xx){
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + xx.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

