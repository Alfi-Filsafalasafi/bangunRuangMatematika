    private void btn_hasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hasilActionPerformed
        try  {
            double luasP = Double.parseDouble(txt_luasP.getText());
            double luas = Double.parseDouble(txt_luas.getText());
            double kel = Double.parseDouble(txt_kelalas.getText());
            double LP_LA;
            PrismaSegitiga prisma = new PrismaSegitiga();
            prisma.setKelAlas(kel);
            prisma.setLuasPermukaan(luasP);
            prisma.setLuasAlas(luas);
            LP_LA = prisma.getLuasPermukaan() - 2 * prisma.getLuasAlas();
            DecimalFormat df = new DecimalFormat("#.##");
            txt_hasil.setText(String.valueOf(df.format(prisma.TinggiPris_L())));
            txt_proses.setText("Tinggi Prisma =  (  "+prisma.getLuasPermukaan()+" -  ( 2 x "+prisma.getLuasAlas()+" )  )  /  "+prisma.getKelAlas()+ "\n"
                              +"Tinggi Prisma =  (  "+prisma.getLuasPermukaan()+" - "+2*prisma.getLuasAlas()+"  )  /  "+prisma.getKelAlas() + "\n"
                              +"Tinggi Prisma =  "+LP_LA+"  /  "+prisma.getKelAlas() + "\n"
                              +"Tinggi Prisma =  "+prisma.TinggiPris_L());
        }
        catch(Exception xx){
            JOptionPane.showMessageDialog(null,"Inputan anda kosong / tidak sesuai , Keterangan : " + xx.getMessage());
        }
    }//GEN-LAST:event_btn_hasilActionPerformed

