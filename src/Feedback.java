    private void btn_kirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kirimActionPerformed
        if(txt_nama.getText().equals("") || txt_masukan.getText().equals("")){
           JOptionPane.showMessageDialog(this, "Nama atau Masukkan anda masih kosong"); 
        }else{
            try {
            String sql = "INSERT INTO Masukan(nama,keterangan) VALUES ('" + txt_nama.getText() 
                    + "','" + txt_masukan.getText() + "')";
            java.sql.Connection conn = (Connection) koneksi.koneksiDB();

 
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Masukan telah terkirim");
            txt_nama.setText("");
            txt_masukan.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal terhubung ke server . Pastikan anda terhubung "
                        + "ke internet atau server lagi sibuk");
            }
        }
        
    }//GEN-LAST:event_btn_kirimActionPerformed

